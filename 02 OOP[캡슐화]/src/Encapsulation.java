
public class Encapsulation {
	/* OOP 의 클래스 설계 방법 
	 *   1. 캡슐화 Encapsulation
	 *   2. 상속성 Inheritance
	 *   3. 다형성(오버로딩,오바라이딩,객체형변환) Polymorphism
	 */
	

	/*
	 * 1. 캡슐화
	 *    - 외부 클래스에서 멤버변수에 접근을 막고
	 *      멤버 메쏘드에만 접근할 수 있도록 클래스를 설계하는 방법
	 *    - 구현 : 멤버변수의 접근제한자를 private 멤버변수의
	 *             접근제한자는 public 으로한다. 
	 *             public ==> 어떤 외부클래스(객체)에서든지 접근 가능
	 *             private==> 어떤 외부클래스(객체)에서든지 접근 불가능
	 */
}
